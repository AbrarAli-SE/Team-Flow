import { organization_user } from "@kinde/management-api-js";
import { Avatar, AvatarFallback } from "@/components/ui/avatar";
import Image from "next/image";
import { getAvatar } from "@/lib/get-avatar";
import { cn } from "../../../../../../../../lib/utils";

interface MemberItemProps {
  member: organization_user;
  isOnline?: boolean;
}

export function MemberItem({ member, isOnline }: MemberItemProps) {

  const isAdmin = member.roles?.includes('admin');
  return (
    <div className="px-3 py-2 hover:bg-accent cursor-pointer transition-colors">
      <div className="flex items-center space-x-3">
        <div className="relative">
          <Avatar className="size-8">
            <Image
              src={getAvatar(member.picture ?? null, member.email!)}
              alt="Member Avatar"
              fill
            />
            <AvatarFallback>
              {member.full_name?.charAt(0).charAt(1).toUpperCase()}
            </AvatarFallback>
          </Avatar>
          {/* online / offline indicators */}
          <div
            className={cn(
              "absolute bottom-0 right-0 size-2.5 rounded-full border-2 border-background bg-violet-500",
              isOnline
                ? "bg-green-500"
                : "bg-gray-400"
            )}
          ></div>
        </div>

        {/* Member Info */}

        <div className="flex-1 min-w-0">
          {isAdmin ? (
             <div className="flex items-center justify-between">
            <p className="font-medium text-sm truncate">{member.full_name}</p>
            <span className="inline-flex items-center rounded-md bg-blue-50 text-blue-700 px-2 py-1 text-xs font-medium right-1 ring-inset ring-blue-700/10 dark:bg-blue-400/10 dark:text-blue-400 dark:ring-blue-400/30">
              Admin
            </span>
          </div>
          ) : (
             <div className="flex items-center justify-between">
            <p className="font-medium text-sm truncate">{member.full_name}</p>
            <span className="inline-flex items-center rounded-md bg-blue-50 text-gray-700 px-2 py-1 text-xs font-medium right-1 ring-inset ring-gray-700/10 dark:bg-gray-400/10 dark:text-gray-400 dark:ring-gray-400/30">
              User
            </span>
          </div>
          )}
          <p className="text-xs text-muted-foreground truncate ">
            {member.email}
          </p>
        </div>
      </div>
    </div>
  );
}
